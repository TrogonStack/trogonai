//! Emit Helm-chart CRD YAML for every Trogon-owned `CustomResource`.
//!
//! Upstream-owned CRDs (Gateway API `Gateway`, `HTTPRoute`) are
//! installed separately via the official Gateway-API release manifests
//! and therefore are not emitted here.

use std::fs;
use std::path::{Path, PathBuf};

use clap::Parser;
use kube::CustomResourceExt;

use trogon_gateway_k8s::crd::{AgentgatewayBackend, EnterpriseAgentgatewayPolicy, MCPGatewayConfig};

fn default_out_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../../../charts/agentgateway-crds/templates")
}

#[derive(Debug, Parser)]
#[command(name = "crdgen", about = "Emit Helm-chart CRD YAML for Trogon CRDs")]
struct Args {
    /// Destination directory (Helm chart `templates/`).
    #[arg(long, env = "CRDGEN_OUT_DIR")]
    out_dir: Option<PathBuf>,
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let args = Args::parse();
    let out_dir = args.out_dir.unwrap_or_else(default_out_dir);
    fs::create_dir_all(&out_dir)?;

    write_crd::<MCPGatewayConfig>(&out_dir)?;
    write_crd::<EnterpriseAgentgatewayPolicy>(&out_dir)?;
    write_crd::<AgentgatewayBackend>(&out_dir)?;

    Ok(())
}

const CRDGEN_HEADER: &str = "# Generated by `cargo run -p trogon-gateway-k8s --bin crdgen`. DO NOT EDIT.\n\
                             # Run `mise run crdgen` to regenerate.\n";

fn write_crd<K: CustomResourceExt>(out_dir: &Path) -> Result<(), Box<dyn std::error::Error>> {
    let crd = K::crd();
    let name = crd.metadata.name.clone().ok_or("CRD has no metadata.name")?;
    let path = out_dir.join(format!("{name}.yaml"));
    let yaml = serde_norway::to_string(&crd)?;
    fs::write(&path, format!("{CRDGEN_HEADER}{yaml}"))?;
    println!("wrote {}", path.display());
    Ok(())
}
